# Importing permutation
from itertools import permutations

# Making permutations
perm = permutations([1, 2, 3, '*', '+'])

# Converting itertools object to list
perm = list(perm)

# This list will save all valid permutations (start & end with number)
valid_perms = []


for i in perm:

    # adding all valid permutations (start & end with number) to valid_perms
    if str(i[0]).isdigit() and str(i[-1]).isdigit():
        valid_perms.append(i)

# This list will store final and totally valid permutations (number,operator,number,operator,number)
final_perms = []

for i in valid_perms:

    # adding all final andvalid permutations (number,operator,number,operator,number) to final_perms
    if type(i[1]) == (str) and type(i[-2]) == (str):
        final_perms.append(i)


# Dummy checking for some asnwer
answer = 7

# Looping over final_perms to check for correct permutation
for j in final_perms:

    # converting to list and add parenthesis pair for first 2 numbers to tackle operator precedence
    j = list(j)
    j.insert(0, '(')
    j.insert(4, ')')

    # converting the list to string
    expression = "".join(map(str, j))

    # checking and printing that permutation
    if eval(expression) == answer:
        print(expression)

